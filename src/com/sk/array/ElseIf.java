package com.sk.array;

public class ElseIf {
	public static void main(String[] args) {

		int i = 5;

		if (i==3) {
			System.out.println("3");

		} else if (i == 5) {
			System.out.println("4");

		} else if (i == 5) {
			System.out.println("2");

		} else {
			System.out.println("nothing");
		}

	}
}
